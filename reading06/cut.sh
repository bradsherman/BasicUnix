TLDR - cut
=====

Overview
--------

used to remove lines or sections of lines from a file or files

Examples
--------

- Used to remove the 3-5 fields of exampl.txt

    $cut -f 3-5 example.txt

- uts the first 10 characters of every line of example.txt

    $cut -c 1-10 example.txt

